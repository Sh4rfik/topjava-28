package ru.javawebinar.topjava.service.datajpa;

import org.junit.Test;
import org.springframework.test.context.ActiveProfiles;
import ru.javawebinar.topjava.MealTestData;
import ru.javawebinar.topjava.UserTestData;
import ru.javawebinar.topjava.model.User;
import ru.javawebinar.topjava.service.UserAbstractServiceTest;
import ru.javawebinar.topjava.util.exception.NotFoundException;
import org.junit.Assert;

import static ru.javawebinar.topjava.MealTestData.MEAL_MATCHER;
import static ru.javawebinar.topjava.Profiles.DATAJPA;
import static ru.javawebinar.topjava.UserTestData.*;

@ActiveProfiles(DATAJPA)
public class DataJpaUserServiceTest extends UserAbstractServiceTest {

    @Test
    public void getUserWithMeals() {
        User user = service.getUserWithMeals(USER_ID);
        USER_MATCHER.assertMatch(user, UserTestData.user);
        MEAL_MATCHER.assertMatch(user.getMeals(), MealTestData.meals);
    }

    @Test
    public void getUserWithMealsNotFound() {
        Assert.assertThrows(NotFoundException.class,
                () -> service.getUserWithMeals(NOT_FOUND));
    }
}